    @staticmethod
    def get_adaptive_color(base_hex):
        hour = datetime.datetime.now().hour
        # 夜(18-5時)はベース色を暗く、昼はそのまま
        if 18 <= hour or hour < 5:
            return f"color-mix(in srgb, {base_hex}, black 60%)"
